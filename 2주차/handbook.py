# 🐍 Python 기초 문법 요약 (수업 복습용)
#    - print, input, 변수 선언, if, while, int 함수 등 사용법 포함
# ─────────────────────────────────────────────────────────────

# 📌 print()
#    → 화면에 내용을 출력합니다.
#    예: print("안녕하세요")  → 안녕하세요 출력

# 📌 input()
#    → 사용자로부터 값을 입력받습니다 (항상 문자열로 입력됩니다!)
#    예: name = input("이름을 입력하세요: ")

# 📌 변수 선언 (데이터 저장)
#    → 데이터를 저장할 이름을 정하고 값을 넣습니다.
#    예: age = 17  → age라는 이름에 17이 저장됨

# 📌 int()
#    → 문자열을 정수로 바꿔줍니다. 숫자 계산할 때 꼭 필요!
#    예: int("3") → 3 (문자 → 숫자)

# 📌 if 문 (조건문)
#    → 조건에 따라 다른 코드를 실행할 수 있습니다.
#    예:
#       if age >= 20:
#           print("성인입니다.")
#       else:
#           print("미성년자입니다.")

# 📌 while 문 (반복문)
#    → 조건이 참(True)일 동안 계속 반복 실행됩니다.
#    예:
#       i = 1
#       while i <= 5:
#           print(i)
#           i += 1

